<p>Dinus Open Source Community (DOSCOM) merupakan sebuah komunitas yang terdiri dari mahasiswa, pelajar dan masyarakat yang aktif dalam penggunaan Open Source Software (OSS). Komunitas ini dulunya bernama Gabungan Linuxer Mahasiswa Dian Nuswantoro (GLADIATOR) yang dibentuk pada tanggal 27 November 2003 oleh Universitas Dian Nuswantoro. Setelah vakum selama 2 tahun, pada tanggal 16 Oktober 2007 DOSCOM aktif kembali dan menjadi salah satu provokator dalam memasyarakatkan dunia Teknologi Informasi dengan Open Source Software (OSS) di Indonesia. Dengan dukungan penuh dari Kementrian Riset dan Teknologi Republik Indonesia dan Jawa Tengah Open Source Center, DOSCOM dengan misinya memasnyarakatkan open source dan mengopen-sourcekan masyarakat bertekat kuat untuk berbagi, dan menyebarkan ilmu open source kepada masyarakat.</p>
